    private void jbAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbAgregarActionPerformed
        // TODO add your handling code here:
        int dni= Integer.parseInt(jtDNI.getText());
        String nombre= jtNombre.getText();
        String apellido= jtApellido.getText();
        String cuidad= jtCiudad.getText();
        String domicilio= jtDomicilio.getText();
        
        Cliente pers= new Cliente(dni, nombre, apellido, cuidad, domicilio);
        
       String numero= jtTelefono.getText();
       
       
       Directorio dir=Menu.getDirectorio();
        if(dir.agregarCliente( numero, pers)){
            JOptionPane.showMessageDialog(this,"Cliente agregado exitosamente" );
            limpiar();
        }else{
            JOptionPane.showMessageDialog(this,"Cliente ya ingresado con el mismo N¬∞ de telefono" );
        }
         
        
        
    }//GEN-LAST:event_jbAgregarActionPerformed

