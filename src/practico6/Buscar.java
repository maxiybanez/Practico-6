    private void jbBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbBuscarActionPerformed
        // TODO add your handling code here:
        Directorio dir=Menu.getDirectorio();
        String telefonoBusca= jtIngreso.getText();
        Cliente clienteBuscado= dir.buscarCliente(telefonoBusca);
                if(clienteBuscado!=null){
                    jtApellidoMostrar.setText(clienteBuscado.getApellido());
                    jtNombre.setText(clienteBuscado.getNombre());
                    jtDomicilio.setText(clienteBuscado.getDomicilio());
                    jtCuidadMostrar.setText(clienteBuscado.getCiudad());
                    jtDni.setText(valueOf(clienteBuscado.getDni()));
                    jtTelefono.setText(telefonoBusca);
                                       
                }else{
                    JOptionPane.showMessageDialog(this,"Cliente no se encuentra en el directorio" );
                }
        
     
        
        
    }//GEN-LAST:event_jbBuscarActionPerformed

