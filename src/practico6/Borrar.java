    private void jbBorrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbBorrarActionPerformed
        // TODO add your handling code here:
        String[] options = {"Si","No"};
        int seleccion = JOptionPane.showOptionDialog(null, "¬øDesea borrar el cliente buscado?", "", JOptionPane.DEFAULT_OPTION, JOptionPane.QUESTION_MESSAGE, null, options, options[0]);
          if(seleccion==0){
          listaBorrado.remove(jtTelefono.getText());
          JOptionPane.showMessageDialog(this,"Cliente borrado" );
          limpiar();
          }else{
              int dni=Integer.parseInt(jtDniBorrar.getText());
              Cliente clienteRecuperado= new Cliente(dni, jtNombre.getText(), jtApellido.getText(), jtCuidad.getText(), jtDomicilio.getText());
              dir.agregarCliente(jtTelefono.getText(), clienteRecuperado);
              
          }
        
         
    }//GEN-LAST:event_jbBorrarActionPerformed

