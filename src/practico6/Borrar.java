    private void jbBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbBuscarActionPerformed
        
        int dni=Integer.parseInt(jtDniBorrar.getText());
        listaBorrado=dir.borrarCliente(dni);
        Set <String> telefonoRec=listaBorrado.keySet();
            if(listaBorrado.size()>0){
                for(String telefono:telefonoRec){
                    jtApellido.setText(listaBorrado.get(telefono).getApellido());
                    jtNombre.setText(listaBorrado.get(telefono).getNombre());
                    jtCuidad.setText(listaBorrado.get(telefono).getCiudad());
                    jtDomicilio.setText(listaBorrado.get(telefono).getDomicilio());
                    jtTelefono.setText(telefono);
                 }
            }else{
                    JOptionPane.showMessageDialog(this,"No se encontro Cliente con ese DNI,intente de nuevo." );
            }
    }//GEN-LAST:event_jbBuscarActionPerformed

